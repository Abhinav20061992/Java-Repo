
public class Conditional_Class {

		public static void main(String[] args) {
//Illustration of if-else			
			int a=28;
			int b=27;
			
			if (a==b) {
				System.out.println("They are equal");				
			}
			else { System.out.println("They are not equal");}
			
//Illustration of for loop
			for(int i=1;i<a;i++) {
				System.out.println(i);
			}
			
//Illustration of switch 			
			 int number=20;  
			      
			    switch(number){  
			    
			    case 10: System.out.println("18");  
			    break;  
			    case 20: System.out.println("28");  
			    break;  
			    case 30: System.out.println("38");  
			    break;  
			    
			    default:System.out.println("Not in 10, 20 or 30");  
			    }  
			    
//Illustration of while loop			  
			    
			    int i=29;  
			    while(i>=1){  
			        System.out.println(i);  
			    i--;  
			    }  
		}

	}
